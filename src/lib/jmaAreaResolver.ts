/**
 * jmaAreaCode から警報 API に使う6桁都道府県コードを導出する。
 * 例: "1310100" → "130000"（東京）
 * 北海道（01）・鹿児島（46）・沖縄（47）は個別マッピングを使用する。
 */
export function prefCodeFromAreaCode(areaCode: string): string {
  const override = AREA_TO_PREF[areaCode];
  if (override) return override;
  const pref2 = areaCode.slice(0, 2);
  // 鹿児島の大多数（本島地方）は 460100
  if (pref2 === '46') return '460100';
  // 沖縄の大多数（本島地方）は 471000
  if (pref2 === '47') return '471000';
  return `${pref2}0000`;
}
